public class Operators {
    public static void main(String[] args) {
        int a = 4;
        int b = 54 + a;
        int d = 54 * a;

        System.out.println(b);
        System.out.println(d);
        System.out.println(6 == 8);
        System.out.println(64 > 9);
        // logical operators
        System.out.println(43 > 9 && 43 > 5);
        System.out.println(43 > 7 || 32 > 7);

    }
}
